    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
            // TODO add your handling code here:
            try {
                String id = txtID.getText();
                String type = String.valueOf(txtType.getSelectedItem());
                float width = Float.valueOf(txtWidth.getText());
                float deep = Float.valueOf(txtDeep.getText());
                float large = Float.valueOf(txtLarge.getText());
                model estanque = new model(id, large, width, deep, type);
                this.cleanText();
                this.showInfo(estanque);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Rellene todos los campos");
            }
            
    }//GEN-LAST:event_jButton1ActionPerformed
    
