    private void cleanText(){
        this.txtDeep.setText("");
        this.txtWidth.setText("");
        this.txtLarge.setText("");
        this.txtID.setText("");
    }
    
    private void showInfo(model estanque){
        listResume.addElement("ID: " + estanque.getIdentificacion());
        listResume.addElement("Width: " + estanque.getWidth() + "m");
        listResume.addElement("Large: " + estanque.getLarge() + "m");
        listResume.addElement("Deep: " + estanque.getDeep() + "m");
        listResume.addElement("Capacidad Agua: " + estanque.calcAgua() + "m3");
        listResume.addElement("Alevinos: " + estanque.calcAlevinos());
        listResume.addElement("Alimento: " + estanque.calcAlimento() + " kg");
        listResume.addElement("===========================");
    }
        
